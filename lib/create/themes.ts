export type ThemeName = (typeof THEMES)[number]["name"];

// The 7 base colors (neutral tones used as base)
export const BASE_COLOR_NAMES = [
	"neutral",
	"stone",
	"zinc",
	"mauve",
	"olive",
	"mist",
	"taupe",
] as const;

export type BaseColorName = (typeof BASE_COLOR_NAMES)[number];

export const BASE_COLORS = THEMES.filter((t) =>
	BASE_COLOR_NAMES.includes(t.name as BaseColorName),
);

export function getThemesForBaseColor(baseColorName: string): Theme[] {
	return THEMES.filter((theme) => {
		if (theme.name === baseColorName) return true;
		return !BASE_COLOR_NAMES.includes(theme.name as BaseColorName);
	});
}

export function getTheme(name: string): Theme | undefined {
	return THEMES.find((t) => t.name === name);
}

export function getBaseColor(name: string): Theme | undefined {
	return BASE_COLORS.find((t) => t.name === name);
}
