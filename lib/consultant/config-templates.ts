export interface SkillsManifestTemplateParams {
	skills: RegistrySkill[];
	projectName: string;
}

/**
 * Generates a .claude/skills/SKILLS.md manifest listing all active skills.
 * This is a reference document — full skill SKILL.md files are not generated
 * (they live in the vantage-registry and are fetched on demand).
 */
export function skillsManifestTemplate(
	params: SkillsManifestTemplateParams,
): string {
	const byCategory = params.skills.reduce<Record<string, RegistrySkill[]>>(
		(acc, skill) => {
			const cat = skill.category;
			if (!acc[cat]) acc[cat] = [];
			acc[cat].push(skill);
			return acc;
		},
		{},
	);

	const categoryBlocks = Object.entries(byCategory)
		.sort(([a], [b]) => a.localeCompare(b))
		.map(([category, skills]) => {
			const rows = skills
				.map((s) => `| \`${s.id}\` | ${s.name} | ${s.description} |`)
				.join("\n");
			return `### ${category.charAt(0).toUpperCase() + category.slice(1)}\n\n| ID | Name | Description |\n|----|------|-------------|\n${rows}`;
		})
		.join("\n\n");

	return `# Active Skills — ${params.projectName}

This manifest lists all skills activated for this project via VantageStarter Consultant Onboarding.

Agents should load individual skill files from the vantage-registry when executing tasks.

---

## Skills by Category

${categoryBlocks}

---

Total: **${params.skills.length} skills** across **${Object.keys(byCategory).length} categories**.
`;
}
