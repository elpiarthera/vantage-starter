export interface SessionStartTemplateParams {
	projectName: string;
	clientName: string;
	sector: string;
	agentIds: string[];
}

/**
 * Generates a customised hooks/session-start.py for the client project.
 * Prints project context and active agents on every session start.
 */
export function sessionStartTemplate(
	params: SessionStartTemplateParams,
): string {
	const agentList = params.agentIds.map((id) => `    - ${id}`).join("\n");

	return `#!/usr/bin/env python3
"""
Session Start Hook — ${params.projectName}

Prints project context and active agents on every Claude Code session start.
Generated by VantageStarter Consultant Onboarding.
"""

import json
import sys

def main():
    try:
        hook_input = json.loads(sys.stdin.read())
    except (json.JSONDecodeError, ValueError):
        hook_input = {}

    context_lines = [
        "=" * 60,
        f"Project: ${params.projectName}",
        f"Client:  ${params.clientName}",
        f"Sector:  ${params.sector}",
        "",
        "Active agents:",
${agentList.replace(/^/gm, "        ").trim() || '        "    (none)"'},
        "=" * 60,
        "",
        "ORCHESTRATION REMINDER:",
        "- Delegate to specialist agents. Do not write work product yourself.",
        "- Read CLAUDE.md before every session for routing rules.",
        "=" * 60,
    ]

    for line in context_lines:
        print(line, file=sys.stderr)

    # Pass through — do not block
    print(json.dumps(hook_input))

if __name__ == "__main__":
    main()
`;
}

