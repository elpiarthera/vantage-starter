def _strip_command_prefixes(tokens):
    """Drop leading assignments, shell keywords and wrapper commands.

    Loops until the head token is a real program (or tokens exhausted),
    returning the remaining argv whose head is the program to inspect.
    """
    toks = list(tokens)
    while toks:
        head = toks[0]

        # leading VAR=value assignment
        if re.match(r"^[A-Za-z_][A-Za-z0-9_]*=", head):
            toks.pop(0)
            continue

        # leading shell keyword / bare brace / paren
        if head in SHELL_KEYWORDS:
            toks.pop(0)
            continue

        # leading wrapper command
        if os.path.basename(head) in WRAPPER_COMMANDS:
            wrapper = os.path.basename(head)
            toks.pop(0)  # drop the wrapper token itself
            # drop the wrapper's own following tokens
            while toks:
                nxt = toks[0]
                if wrapper == "env" and re.match(
                    r"^[A-Za-z_][A-Za-z0-9_]*=", nxt
                ):
                    toks.pop(0)  # env VAR=value
                    continue
                if nxt.startswith("-"):
                    # value-taking flags consume the next token
                    if nxt in WRAPPER_VALUE_FLAGS and len(toks) > 1:
                        toks.pop(0)
                        toks.pop(0)
                    else:
                        toks.pop(0)
                    continue
                break
            continue

        # head is a real program
        break
    return toks


def _segment_covered_regex(segment):
    """v1.0.1 raw-string regex fallback (used when shlex cannot tokenize)."""
    return bool(COVERED_GIT.search(segment) or COVERED_GH.search(segment))


def _tokens_covered(tokens):
    """True if tokenized argv is a covered git authoring subcommand
    (commit|merge|tag|revert|cherry-pick|rebase|am) or
    gh-pr-{create,edit,merge}."""
    # Normalise the head: drop leading assignments, shell keywords and wrappers.
    tokens = _strip_command_prefixes(tokens)
    if not tokens:
        return False

    prog = os.path.basename(tokens[0])
    args = tokens[1:]

    if prog == "git":
        i = 0
        while i < len(args):
            tok = args[i]
            if tok in GIT_VALUE_OPTS:
                i += 2  # skip option AND its separate value
                continue
            if tok.startswith("-"):
                i += 1  # skip lone option (incl. --git-dir=/x, -c k=v inline, --no-pager)
                continue
            return tok in GIT_AUTHORING_SUBCMDS  # first non-option = subcommand
        return False

    if prog == "gh":
        non_opts = []
        i = 0
        while i < len(args) and len(non_opts) < 2:
            tok = args[i]
            if tok.startswith("-"):
                i += 1
                continue
            non_opts.append(tok)
            i += 1
        return (
            len(non_opts) >= 2
            and non_opts[0] == "pr"
            and non_opts[1] in ("create", "edit", "merge")
        )

    return False


def _tokens_covered_any_segment(command):
    """Precise tokenizer path. True if ANY segment is a covered command."""
    if not command:
        return False
    for segment in _split_segments(command):
        segment = segment.strip()
        if not segment:
            continue
        try:
            tokens = shlex.split(segment, comments=False)
        except ValueError:
            # Unbalanced quotes / heredoc — preserve v1.0.1 regex coverage.
            if _segment_covered_regex(segment):
                return True
            continue
        if not tokens:
            continue
        if _tokens_covered(tokens):
            return True
    return False


def is_covered_command(command):
    """Scope detection: precise tokenizer path OR fail-closed backstop.

    Runs on the continuation-normalized command so a `git \\<newline> commit`
    is not fragmented. The precise path stays for accurate stderr reporting; the
    backstop guarantees no unrecognised invocation form slips through.
    """
    if not command:
        return False
    normalized = _normalize_continuations(command)
    return (
        _tokens_covered_any_segment(normalized)
        or _backstop_covered(normalized)
    )


def _log_error():
    try:
        with open(ERROR_LOG, "a") as fh:
            fh.write(traceback.format_exc())
    except Exception:
        pass


def _warn_fail_open(reason):
    """Announce a NON-nominal fail-open on stderr (mode 3 of the three modes).

    Writes exactly ONE line so a silent open is never mistaken for a clean pass.
    Best-effort: never raises from inside (a failure to warn must not itself
    crash the user's shell). This is IN ADDITION to `_log_error()` — it does not
    replace the traceback log.
    """
    try:
        sys.stderr.write(
            f"enforce-no-claude-trailer: FAIL-OPEN (not checked) — {reason}. "
            "The command was ALLOWED without an attribution check.\n"
        )
    except Exception:
        pass


def has_override(command):
    """True if a valid override marker (reason >= 6 chars) is present."""
    m = OVERRIDE_RE.search(command)
    if not m:
        return False
    return len(m.group(1).strip()) >= 6


def contains_violation(text):
    if not text:
        return False
    normalized = _normalize_for_predicate(text)
    for pat in VIOLATION_PATTERNS:
        # Predicate runs on the normalized text (separators collapsed). The
        # domain pattern also runs on the raw text so a normalization-adjacent
        # edge cannot hide `anthropic.com`.
        if pat.search(normalized) or pat.search(text):
            return True
    return False


def _read_file_safe(path):
    """Read a file's content; return "" on any error (fail-safe)."""
    try:
        with open(path, "r", errors="replace") as fh:
            return fh.read()
    except Exception:
        return ""


def _extract_flag_files(tokens):
    """Yield file paths referenced by -F/--file/--body-file flags."""
    file_flags = {"-F", "--file", "--body-file"}
    for i, tok in enumerate(tokens):
        if tok in file_flags and i + 1 < len(tokens):
            nxt = tokens[i + 1]
            if nxt != "-":  # `-F -` means heredoc/stdin, not a file
                yield nxt
        else:
            for flag in ("--file=", "--body-file="):
                if tok.startswith(flag):
                    yield tok[len(flag):]


def gather_scan_text(command):
    """Collect all text that should be scanned for a covered command.

    Includes the raw command (covers inline -m, heredoc bodies, --body/-b/
    --subject inline values) plus the content of any referenced files.
    """
    parts = [command]

    # Try to tokenize to find file-flag arguments. shlex may fail on heredocs;
    # fall back to a regex sweep for file flags in that case.
    try:
        tokens = shlex.split(command, comments=False)
    except ValueError:
        tokens = []

    file_paths = list(_extract_flag_files(tokens))
    if not file_paths:
        for m in re.finditer(r"(?:-F|--file|--body-file)[=\s]+(\S+)", command):
            arg = m.group(1)
            if arg != "-":
                file_paths.append(arg)

    for path in file_paths:
        content = _read_file_safe(path)
        if content:
            parts.append(content)

    return "\n".join(parts)


def evaluate_command(command):
    """Return the exit code (0 allow / 2 block) for a bash command."""
    if not command or not isinstance(command, str):
        return 0

    if not is_covered_command(command):
        return 0

    scan_text = gather_scan_text(command)
    if not contains_violation(scan_text):
        return 0

    # Violation present — honor a valid override, else block.
    if has_override(command):
        return 0

    return 2


def _matched_pattern(text):
    normalized = _normalize_for_predicate(text)
    for pat in VIOLATION_PATTERNS:
        if pat.search(normalized) or pat.search(text):
            return pat.pattern
    return "unknown"


def main():
    try:
        raw = sys.stdin.read()
    except Exception:
        _log_error()
        _warn_fail_open("stdin unreadable")
        return 0

    # Empty / whitespace-only stdin is the harness's normal no-op invocation —
    # nothing to check, so this is a NOMINAL silent allow (mode 2), NOT a
    # could-not-look fail-open. Keep it silent by design.
    if not raw or not raw.strip():
        return 0

    try:
        payload = json.loads(raw)
    except (ValueError, TypeError):
        _warn_fail_open("stdin is not valid JSON")
        return 0

    if not isinstance(payload, dict):
        _warn_fail_open("payload is not a JSON object")
        return 0

    try:
        command = payload.get("tool_input", {}).get("command", "")
        code = evaluate_command(command)
        if code == 2:
            matched = _matched_pattern(gather_scan_text(command))
            sys.stderr.write(
                "BLOCKED by enforce-no-claude-trailer: Anthropic/Claude "
                "attribution detected in a commit/PR authoring command.\n"
                f"  Matched pattern: {matched}\n"
                "  Doctrine (Day 123): no Anthropic/Claude attribution anywhere.\n"
                "  If you are WRITING a commit/PR: remove the Co-Authored-By / "
                "noreply@anthropic.com / 'Generated with Claude' trailer.\n"
                "  If you are AUDITING the trailer (a command that SEARCHES for "
                "it, e.g. `git log --grep`, `grep -c 'Co-Authored-By: Claude'`) "
                "or quoting history verbatim: this is an accepted false "
                "positive. Append the override to pass:\n"
                "    // allow-claude-trailer: audit forensique du trailer\n"
                "  Override accepts any reason >= 6 chars; the two legitimate "
                "classes are forensic audit of the trailer and quoting history "
                "verbatim.\n"
            )
        return code
    except Exception as exc:
        _log_error()
        _warn_fail_open(f"unexpected error: {type(exc).__name__}")
        return 0


if __name__ == "__main__":
    sys.exit(main())
