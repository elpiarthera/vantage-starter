r"""enforce-no-claude-trailer — PreToolUse(Bash) Claude Code hook.

DESIGN REVISION v1.1.0 — predicate + scope inversion.
------------------------------------------------------
VERSION HONESTY (read before trusting any number here). This docstring labels a
DESIGN revision (`v1.1.0 — predicate + scope inversion`). It is NOT the VR
`contentVersion`. The VR `contentVersion` is an INDEPENDENT backend patch
counter incremented automatically by `bumpPatch` on any byte change — we cannot
set it, and it has read `1.0.2` while an older docstring lied `v1.0.3`. Do not
compare the two. The CANONICAL IDENTITY of this file is its `contentHash`
(SHA-256 of the exact bytes). RULE #30 conformance (sha256(local) == VR
contentHash) MUST be checked on the HASH, never on the version field. The
version string in this docstring is a human-facing design label only.

WHAT v1.1.0 CHANGES (two-axis fix). For six versions the SCOPE axis (which
invocation forms are covered) was hardened while the PREDICATE axis (what counts
as attribution) never moved — `VIOLATION_PATTERNS` was three enumerated regexes,
frozen since v1.0.0. The fail-closed inversion had been applied only to the
invocation-form axis; the attribution-form axis stayed fail-OPEN, so
`Generated by Claude Code` LEAKED while `Generated with Claude Code` blocked —
one word apart, same attribution. v1.1.0 inverts the posture on BOTH axes:

  PREDICATE (attribution axis) — now a FAMILY PREDICATE, not an enumeration.
  A single case-insensitive regex recognises the attribution FAMILY:
      family-verb  +  MANDATORY preposition  +  Claude/Anthropic (short window).
  The mandatory preposition (`by|with|via|using`) is what separates a
  well-formed attribution family from a catch-all: `created Anthropic client`
  (family verb, NO preposition) is ordinary prose and must NOT match, whereas
  `created by Claude` (verb + preposition + entity) is attribution and MUST.
  Requiring the preposition lets the predicate be broad on verbs and phrasings
  without swallowing every sentence that merely names a verb near "Anthropic".
  A separate `anthropic\.com` domain pattern stays as its own list entry.

  SCOPE (invocation axis) — commit-authoring git subcommands now ALL in scope.
  `git merge --no-ff -m`, `git merge -F <file>`, `git tag -a -m`, `git revert`,
  `git cherry-pick`, `git rebase` (and `git am`) each CREATE A COMMIT OBJECT
  whose message can carry the trailer. They are therefore in scope exactly like
  `git commit`: both the backstop and the precise tokenizer accept any of
  {commit, merge, tag, revert, cherry-pick, rebase, am} as the authoring
  subcommand, and `-F`/`--file`/`--body-file` are read for all of them.
  DELIBERATELY OUT OF SCOPE: `gh pr comment --body <...>`. A PR comment is NOT
  authorship of a commit object — reviewers legitimately POST verdicts that
  QUOTE the trailer ("this commit still carries Co-Authored-By: Claude"). Guard
  a comment and you block the review that catches the leak. This omission is a
  written decision, not an oversight.

v1.0.3 (fail-closed backstop + precise path). Enumerating shell invocation
forms is unwinnable — this guard leaked eleven times across four versions, each
fix adding one more recognised wrapper (`eval`, `bash -c`, backticks, `xargs`,
`find -exec`, `ssh`, aliases, `$'...'`, base64, …) only for the next probe to
find another. The design is therefore inverted: **a guard must block when it
does not understand, not pass.**

Two-layer scope detection now runs on the continuation-normalized raw command:

  1. Precise tokenizer path (unchanged) — accurate scope, drives the stderr
     report (which subcommand, which file).
  2. Fail-closed backstop (`_backstop_covered`) — the core of v1.0.3. If the
     whole words `git` AND `commit` both appear, OR `gh` AND `pr` AND one of
     `create|edit|merge` all appear, the command is treated as COVERED
     **regardless of invocation form**. Whatever wrapper hides the call
     (`eval`, backticks, `bash -c`, `sh -c`, `xargs`, `ssh host "…"`, line
     continuations), the words `git`+`commit` and the Anthropic trailer are all
     still present in the raw command text, so the backstop sees them.

  `is_covered_command()` = precise-path-any-segment OR backstop.

Line continuations (`\\` immediately followed by a newline) are normalised to a
single space FIRST — before any segment splitting — but only when NOT inside
single/double quotes. This is required because v1.0.2 made `\n` a segment
separator: without this normalisation a `git \<newline> commit` would fragment
into two segments and slip past the precise path (the exact regression that
manufactured the v1.0.2 leak).

DELIBERATE TRADE-OFF (fail-closed). The backstop may block a command that
merely MENTIONS both words together with a trailer, e.g.
`git log --grep "commit … Co-Authored-By: Claude"`. That is intentional: the
documented override `// allow-claude-trailer: <reason ≥ 6 chars>` is the escape
hatch for the rare verbatim-history case. Blocking a benign mention is cheap;
letting an attribution trailer through is the failure we are eliminating.

ACCEPTED FALSE-POSITIVE CLASS (named). Any command that SEARCHES for the
trailer — the trailer-audit / forensic tooling used to FIND offending commits —
is blocked by the backstop, because the words `git`/`commit` and the trailer
co-occur in the command text (the backstop cannot tell "writes the trailer"
from "greps for the trailer"). Two real examples:

    gh api repos/$R/commits/$sha -q .commit.message \
        | grep -c 'Co-Authored-By: Claude' ; git log --oneline
    git log --grep "commit Co-Authored-By: Claude"

This is accepted, not a defect: the auditor appends the canonical override
reason `// allow-claude-trailer: audit forensique du trailer` (any reason ≥ 6
chars works; this one is self-documenting). The stderr block message names this
class and prints the copy-pasteable line so an auditor is never tempted to
disarm the hook. NOTE: an audit command that does NOT contain the word `commit`
(e.g. `git log -1 --format='%B' $sha | grep -ci 'Co-Authored-By: Claude'`) is
not covered by the backstop at all, so it passes without any override.

RESIDUAL BOUNDARY (honest). The hook sees ONLY the Bash command STRING. A
trailer that is materialised entirely at runtime — read from a file the hook
cannot see, fetched over the network, decoded from an opaque blob, or produced
by a program's output — is out of scope by construction. The backstop closes
the *wrapper/quoting* class (the words are in the command text); it cannot close
the *runtime-materialisation* class (the words are not in the command text).

v1.0.2 (head/separator hardening): segment splitting now also breaks on a
**newline** (`\n`), a bare `&`, and subshell parens `(` / `)` — multi-line Bash
commands (the norm in this harness) and backgrounded/subshelled git commits no
longer slip past the per-segment scope check. Head normalisation now runs
`_strip_command_prefixes`, which loops past leading `VAR=value` assignments,
shell keywords (`if then else … fi do done while … { } ! ( )`) and wrapper
commands with their own options (`env`, `sudo`, `command`, `time`, `nohup`,
`exec`, `builtin`, `nice`, `ionice`, `stdbuf`) before deciding `prog`. This
closes the newline/keyword/`env …`/`sudo …`/`command …`/`time …`/`nohup …`
bypasses. shlex → ValueError → regex fallback preserved for heredocs.

v1.0.2: scope detection is now **tokenizer-based** (shlex), not raw-string regex.
The command is split into segments on shell separators (`&&`, `||`, `;`, `|`),
each segment is tokenized with `shlex.split`, and global git options (with or
without separate values, including quoted paths containing spaces) are walked
token-by-token to find the real subcommand. This closes the quoted-path bypass
(`git -C "dir with space" commit ...`) that any raw-string regex leaves open.
On a shlex `ValueError` (unbalanced quotes / heredocs) the segment falls back to
the v1.0.1 regex behaviour so heredoc coverage (`git commit -F - <<EOF`) stays.

v1.0.1: global git options (`-C <dir>`, `--no-pager`, `-c k=v`) placed between
`git` and `commit` are now inside the guarded scope (previously escaped the guard).

SCOPE
    Refuses any Anthropic/Claude attribution in git commits and GitHub PR
    authoring commands. Doctrine (Laurent, Day 123): no Anthropic/Claude
    attribution anywhere — commits, PR bodies, docs, code comments.

    Reads a JSON payload on stdin of the shape:
        {"tool_name": "Bash", "tool_input": {"command": "<shell command>"}}

    Covered commands (all AUTHOR a commit object whose message can carry the
    trailer, or a PR body that can):
        - git commit / merge / tag / revert / cherry-pick / rebase / am
          (message via -m "...", heredoc `-F - <<EOF`, or -F/--file/--body-file <file>)
        - gh pr create / gh pr edit / gh pr merge  (--body, -b, --body-file, --subject)

    Deliberately NOT covered (written decision, not an oversight):
        - gh pr comment  — a comment is not authorship; reviewers post verdicts
          that QUOTE the trailer, and guarding the comment blocks the review.

    Violation predicate (four precise shapes, case-insensitive) — NORMALIZE-THEN-
    MATCH. The scan text is first lowercased and every run of whitespace
    (including newlines), underscores and hyphens is collapsed to a single space
    (`_normalize_for_predicate`), so spelling/separator variants converge to one
    canonical form BEFORE matching. The predicate is FOUR precise shapes, not one
    loose window:
        1. TRAILER-KEY   — <family-verb>by: <window> Claude/Anthropic.
           family-verb stem = co-authored | signed-off | assisted | generated
                              | authored | written | created | made.
           No word boundary between the verb stem and `by` — this is what closes
           the glued `coauthoredby:` leak (normalization COLLAPSES separators, it
           never INSERTS one, so a `\b…by` could not match a run-together key).
           e.g. "Co-Authored-By: Claude", "Co_Authored_By: Claude",
                "CoAuthoredBy: Claude", "Co-Authored-By : Claude",
                "Assisted-By: Claude Opus 4.8", "Signed-off-by: Claude".
        2. FOOTER-PHRASE — <verb> <connector> (article/bracket?) Claude/Anthropic,
           the entity IMMEDIATELY adjacent to the connector.
           e.g. "Generated by Claude Code", "Made with Claude Code",
                "Generated via Claude", "Powered by Claude",
                "assisted by Anthropic".
        3. ON-BEHALF-OF  — "on behalf of ... Claude/Anthropic" (entity immediate).
        4. DOMAIN        — anthropic.com (separate load-bearing pattern, matched
           on both raw and normalized text).

    THE PRECISION DECISION — agent-adjacency (deliberate trade-off, written down).
        The retired predicate was `verb + connector + <anything within 40 chars>
        + entity`. It never required the entity to be the AGENT of the connector,
        so it fired FALSE POSITIVES on ordinary commit messages:
            "auto-generated by script for Anthropic API"  (agent = script)
            "generated by CI, references Anthropic client" (agent = CI)
            "file created by fixture, mocks Anthropic"     (agent = fixture)
        In each, the true agent of the connector is `script`/`CI`/`fixture`;
        `Anthropic` is merely a later word. Shapes 2 and 3 now REQUIRE the entity
        to be the agent of the connector — adjacent, modulo one article/bracket.
        CONSEQUENCE, accepted on purpose: a contrived prose form such as
            "Generated by the model known as Claude"
        is NO LONGER blocked (the token after the connector is `the model`, not
        the entity). This is a deliberate EXCHANGE: blocking `generated by CI`
        daily would get the hook disarmed, and a disarmed guard is worse than no
        guard because people still believe they are protected — the same argument
        the reviewer used about trailer-audit tooling, now turned toward
        precision. The trailer-key form (Shape 1) is the shape every REAL
        attribution trailer takes; the footer-phrase form (Shape 2) covers the
        marketing footer. Both are covered; distant-prose attribution is out of
        scope AND written down here.

    NAMED-ENTITY BOUNDARY (deliberate, architect-decided). The predicate fires
        ONLY on the NAMED entities `Claude` / `Anthropic` (and the `anthropic.com`
        domain). An UNNAMED reference to the authoring agent — "the model", "the
        assistant", "an AI" — is OUT of scope on purpose. The Day-123 doctrine
        forbids *nominative* attribution to a named third party (Anthropic/Claude):
        the harm is the gratuitous promotion of a NAMED brand. "Generated by the
        assistant" / "Co-Authored-By: an AI" attributes the work to nobody in
        particular — no named third party is promoted — so there is nothing for
        the doctrine to guard. Covering the unnamed case would require an
        open-ended semantic predicate ("does this phrase refer to some AI?"),
        which would fire FALSE POSITIVES on ordinary text ("the assistant manager
        approved", "an AI startup we integrate"). THE BOUNDARY IS THE NAMED
        ENTITY. This sits alongside the other two written boundaries above: the
        distant-prose attribution trade-off (agent-adjacency), and the
        runtime-materialised trailer the hook never sees (RESIDUAL BOUNDARY).

    Two DELIBERATE scope decisions (documented, not accidental):
        - "signed off": a `Signed-off-by: Claude` trailer ascribes authorship/
          endorsement of the commit to Claude exactly as `Co-Authored-By:` does
          (a DCO sign-off naming Claude is attribution) — in scope on purpose.
        - "on behalf of": "on behalf of Anthropic" is an attribution phrase
          (ascribes the work to Anthropic) with no single family verb — in
          scope on purpose, as a distinct grammatical form.

    NON-matches (intentional): "integrate Anthropic API client" (no verb),
        "created Anthropic client wrapper" (verb but NO adjacent connector+entity),
        "written approval from Anthropic legal" (connector `from` not in set),
        "generated by script for Anthropic API" (agent is `script`, not Claude).

OVERRIDE
    Add the marker `// allow-claude-trailer: <reason>` to the command, where
    <reason> is >= 6 characters (reserved for quoting history verbatim).
    A marker with a too-short or absent reason does NOT bypass.

EXIT CODES — three modes, and why the last two must NOT be conflated.
    A guard that does not block MUST say why — otherwise you can never
    distinguish "nothing to flag" from "I could not look". This hook therefore
    has THREE observable modes, not two:

      1. BLOCKS (exit 2). Anthropic/Claude attribution detected in a covered
         command. Writes the full detailed report to stderr.

      2. ALLOWS NOMINALLY (exit 0, stderr SILENT). The normal, healthy case:
         the command is out of scope, or it is covered but carries no
         attribution, or a valid override is present. Nothing to say — the
         guard looked and found nothing. Silence here is correct.

      3. ALLOWS BECAUSE IT COULD NOT LOOK (exit 0, ANNOUNCED on stderr). The
         guard failed OPEN before it could evaluate the command: stdin was
         unreadable, the payload was not valid JSON, the payload was not a JSON
         object, or an unexpected exception fired during evaluation. This is the
         mode that will actually occur in production the day a regex blows up or
         a `-F <file>` read raises something unforeseen. It emits ONE line to
         stderr via `_warn_fail_open(reason)`:
             enforce-no-claude-trailer: FAIL-OPEN (not checked) — <reason>. ...
         so a silent open is never mistaken for a clean pass.

    WHY MODES 2 AND 3 MUST STAY DISTINCT. Conflating "allowed nominally" with
    "allowed because I could not look" is the failure being avoided: a mute
    fail-open opens the guard without a word and nobody ever learns it opened.
    Mode 2 stays silent (nothing to flag); mode 3 announces itself (could not
    look). The exception path ADDITIONALLY logs a full traceback to
    /tmp/enforce-no-claude-trailer.errors.log — the stderr line is IN ADDITION
    to the log, not a replacement.

    EMPTY STDIN STAYS SILENT BY DESIGN. An empty / whitespace-only stdin is the
    harness's normal no-op invocation — there is genuinely nothing to check, so
    it is mode 2 (silent exit 0), NOT mode 3. Do not make it noisy.
"""

