# Normalization for the predicate ONLY (never used by scope detection, never by
# the override check — those must see the raw command text).
#
# NORMALIZE-THEN-MATCH is the structural fix for the separator/whitespace leak
# class. Every prior fix ENUMERATED separators and whitespace inside the regex
# (`co[-\s]?authored`, `[\s\-]*(?:by|with)`), which is an open-ended list: a
# newline between "by" and the entity, underscores instead of hyphens, or two
# spaces each require yet another alternative, and the next probe finds the one
# you missed. Instead we collapse the entire class BEFORE matching: lowercase,
# then squash every run of `[\s_\-]+` (whitespace INCLUDING newlines, plus
# underscores and hyphens) to a single space. `Co_Authored_By`,
# `Co-Authored-By`, `Generated<newline>by` and `Generated   by` all become the
# single canonical string `co authored by` / `generated by`. Spelling variants
# vanish BY CONSTRUCTION — the predicate never enumerates a separator again, so
# there is no "missed alternative" to leak through.
_NORMALIZE_SEP_RE = re.compile(r"[\s_\-]+")


def _normalize_for_predicate(text):
    """Lowercase + collapse every [\\s_\\-]+ run (incl. newlines) to one space.

    Used ONLY by the attribution predicate (contains_violation / _matched_
    pattern). Scope detection and the override check operate on the RAW command
    — normalization would destroy the flag/quoting structure they rely on.
    """
    if not text:
        return ""
    return _NORMALIZE_SEP_RE.sub(" ", text.lower())


# Load-bearing violation predicate (module-level list so the mutation-control
# test can still remove an entry and prove the guard does the work). Patterns
# run against the NORMALIZED text (see _normalize_for_predicate), so `.` safely
# spans former newlines/underscores/hyphens (they are now single spaces).
#
# v1.3.0 — AGENT-ADJACENCY, four precise shapes (replaces the single loose
# `verb + connector + .{0,40}? + entity` predicate that produced false positives).
# The retired predicate never required the entity to be the AGENT of the
# connector: `verb + connector + <anything within 40 chars> + entity` fired on
# `generated by CI, references Anthropic client` because `Anthropic` was merely a
# later word in the sentence — its true agent was `CI`. A guard that blocks
# routine work gets disarmed by the developer, and a disarmed guard is worse than
# no guard (people still believe they are protected). The four shapes below all
# require the entity to be the agent of the connector — adjacent, modulo an
# article/bracket — EXCEPT the trailer-key form, which anchors on a literal
# `<verb>by:` key (the shape every real attribution trailer takes).
#
# Shape 1 — TRAILER-KEY form (the dominant real shape). A key built from a family
#   verb + an optional connector-`by` + a colon, then the entity within a bounded
#   window: `Co-Authored-By: Claude`, `Signed-off-by: Claude`, `Assisted-By:
#   Claude Opus 4.8`. There is NO `\b` between the verb stem and `by` — that is
#   what closes the `coauthoredby:` (no-separator) leak: normalization collapses
#   separators but never INSERTS one, so `\bco ?authored\b by` could never match a
#   glued `coauthoredby`. Built as one alternation of verb stems + ` ?by\s*:`.
#
#   DELIBERATE SCOPE DECISION (signed off): a `Signed-off-by: Claude` DCO trailer
#   ascribes authorship/endorsement to Claude exactly as `Co-Authored-By:` — in
#   scope on purpose.
#
# Shape 2 — FOOTER-PHRASE form. The connector is IMMEDIATELY followed by the
#   entity (modulo one article/bracket): `Generated by Claude Code`, `Made with
#   Claude Code`, `Powered by Claude`, `assisted by Anthropic`. The immediacy is
#   the whole point — it is the AGENT-ADJACENCY property. `generated by script for
#   Anthropic API` does NOT match: the token right after the connector is
#   `script`, so `Anthropic` is not the agent.
#
# Shape 3 — ON-BEHALF-OF form: `on behalf of ... Claude/Anthropic`, entity
#   immediate (modulo `the`). A distinct grammatical attribution form.
#
# Shape 4 — anthropic.com DOMAIN, kept as its own separate entry so the
#   mutation-control test (which drops every pattern whose source mentions
#   "anthropic") can flip a domain case from block to allow. Run against BOTH raw
#   and normalized text (see contains_violation).
_TRAILER_VERB = (
    r"(?:co ?authored|signed ?off|assisted|generated"
    r"|authored|written|created|made)"
)
VIOLATION_PATTERNS = [
    # Shape 1 — trailer-key: <verb>by: <window> entity. No \b between verb & by.
    re.compile(
        r"\b" + _TRAILER_VERB + r" ?by\s*:.{0,40}?\b(?:claude|anthropic)\b",
        re.IGNORECASE,
    ),
    # Shape 2 — footer-phrase: connector immediately followed by the entity.
    re.compile(
        r"\b(?:generated|made|created|written|powered|assisted|authored)"
        r"\s+(?:by|with|via|using)\s+"
        r"(?:\[)?(?:the\s+|a\s+|an\s+)?(?:claude|anthropic)\b",
        re.IGNORECASE,
    ),
    # Shape 3 — on-behalf-of: entity immediate (modulo `the`).
    re.compile(
        r"\bon behalf of\s+(?:the\s+)?(?:claude|anthropic)\b",
        re.IGNORECASE,
    ),
    # Shape 4 — domain.
    re.compile(r"anthropic ?\. ?com", re.IGNORECASE),
]

OVERRIDE_RE = re.compile(r"//\s*allow-claude-trailer:\s*(\S.{5,})")

# git subcommands that author a commit object whose message can carry the
# trailer. Shared by the precise tokenizer, the regex fallback and the backstop.
GIT_AUTHORING_SUBCMDS = (
    "commit", "merge", "tag", "revert", "cherry-pick", "rebase", "am",
)
_GIT_SUB_ALT = "|".join(GIT_AUTHORING_SUBCMDS)

COVERED_GIT = re.compile(
    r"\bgit\b(?:\s+-[^\s]+(?:\s+[^\s-][^\s]*)?)*\s+(?:" + _GIT_SUB_ALT + r")\b"
)
COVERED_GH = re.compile(r"\bgh\s+pr\s+(?:create|edit|merge)\b")

# git global options that consume a SEPARATE next token as their value.
GIT_VALUE_OPTS = {
    "-C", "-c", "--git-dir", "--work-tree", "--namespace",
    "--exec-path", "--config-env",
}

# Shell segment separators (split scope per-segment so `cd /x && git ... commit`
# is evaluated on the git segment). Includes newline, bare `&`, subshell
# parens and backticks so multi-line, backgrounded/subshelled and
# backtick-substituted commits are each evaluated. Splitting is quote-aware
# (see `_split_segments`) so newlines *inside* a quoted commit message do not
# fragment the git segment.
SEGMENT_SPLIT_CHARS = set(";|&\n()`")

# Backstop word-boundary probes (case-sensitive). See _backstop_covered.
_BACKSTOP_GIT = re.compile(r"\bgit\b")
# Any authoring subcommand word co-occurring with `git` closes the backstop.
_BACKSTOP_GIT_SUB = re.compile(r"\b(?:" + _GIT_SUB_ALT + r")\b")
_BACKSTOP_GH = re.compile(r"\bgh\b")
_BACKSTOP_PR = re.compile(r"\bpr\b")
_BACKSTOP_GH_VERB = re.compile(r"\bcreate\b|\bedit\b|\bmerge\b")


def _normalize_continuations(command):
    """Collapse backslash-newline line continuations to a single space.

    Only outside single/double quotes — a `\\<newline>` inside a quoted commit
    message is left intact. Runs BEFORE segment splitting so a
    `git \\<newline> commit` is not fragmented by the newline separator (v1.0.2
    made `\n` a separator; without this it would manufacture a false negative).
    """
    if not command or "\\" not in command:
        return command
    out = []
    quote = None  # None | "'" | '"'
    i = 0
    n = len(command)
    while i < n:
        ch = command[i]
        if quote:
            out.append(ch)
            if ch == quote:
                quote = None
            i += 1
            continue
        if ch in ("'", '"'):
            quote = ch
            out.append(ch)
            i += 1
            continue
        if ch == "\\" and i + 1 < n and command[i + 1] == "\n":
            out.append(" ")
            i += 2
            continue
        out.append(ch)
        i += 1
    return "".join(out)


def _backstop_covered(command):
    """Fail-closed backstop on the continuation-normalized raw command.

    True if the whole word `git` appears together with any commit-authoring
    subcommand word (commit|merge|tag|revert|cherry-pick|rebase|am), OR `gh` AND
    `pr` AND one of `create|edit|merge` all appear. Case-sensitive word-boundary
    search. Rationale: whatever wrapper hides the invocation (`eval`, backticks,
    `bash -c`, `xargs`, `ssh host "…"`, continuations), the words are all still
    present in the raw command text, so an attribution trailer cannot slip
    through an unrecognised form.
    """
    if not command:
        return False
    if _BACKSTOP_GIT.search(command) and _BACKSTOP_GIT_SUB.search(command):
        return True
    if (
        _BACKSTOP_GH.search(command)
        and _BACKSTOP_PR.search(command)
        and _BACKSTOP_GH_VERB.search(command)
    ):
        return True
    return False


def _split_segments(command):
    """Quote-aware split of a command into scope segments.

    Splits on `;`, `|`, `&`, newline and subshell parens `(`/`)` — but only
    when NOT inside single/double quotes, so a newline within a quoted commit
    message keeps the git segment intact. `&&`/`||` collapse naturally (empty
    middle segment is dropped by the caller)."""
    segments = []
    buf = []
    quote = None  # None | "'" | '"'
    for ch in command:
        if quote:
            buf.append(ch)
            if ch == quote:
                quote = None
            continue
        if ch in ("'", '"'):
            quote = ch
            buf.append(ch)
            continue
        if ch in SEGMENT_SPLIT_CHARS:
            segments.append("".join(buf))
            buf = []
            continue
        buf.append(ch)
    segments.append("".join(buf))
    return segments

